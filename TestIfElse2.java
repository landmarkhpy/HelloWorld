public class TestIfElse2 {
	public static void main(String[] args) {
		int x =10;
		if (x == 10) {
			System.out.println("x的值是10");
		} else if (x == 20) {
			System.out.println("x的值是20");
		} else if (x == 30) {
			System.out.println("x的值是30");
		} else {
			System.out.println("这是else语句");
		}

	}
}